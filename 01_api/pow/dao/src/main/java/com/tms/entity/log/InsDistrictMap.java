package com.tms.entity.log;

public class InsDistrictMap {

    private Integer dtmapId;
    private Integer dtId;
    private Integer partnerId;
    private String pnCode;
    private String pnName;
    private String dcsr;

    public Integer getDtmapId() {
        return dtmapId;
    }

    public void setDtmapId(Integer dtmapId) {
        this.dtmapId = dtmapId;
    }

    public Integer getDtId() {
        return dtId;
    }

    public void setDtId(Integer dtId) {
        this.dtId = dtId;
    }

    public Integer getPartnerId() {
        return partnerId;
    }

    public void setPartnerId(Integer partnerId) {
        this.partnerId = partnerId;
    }

    public String getPnCode() {
        return pnCode;
    }

    public void setPnCode(String pnCode) {
        this.pnCode = pnCode;
    }

    public String getPnName() {
        return pnName;
    }

    public void setPnName(String pnName) {
        this.pnName = pnName;
    }

    public String getDcsr() {
        return dcsr;
    }

    public void setDcsr(String dcsr) {
        this.dcsr = dcsr;
    }
}
